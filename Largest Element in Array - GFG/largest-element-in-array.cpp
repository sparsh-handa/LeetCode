class Solution
{
public:
    int largest(vector<int> &arr, int n)
    { 
        int greatest = arr[0];
        for(int i=0;i<n-1;i++)
        {
            if(arr[i]<arr[i+1]){
                greatest = max(greatest,arr[i+1]);
            }
        }
        
        return greatest;
    }
};


